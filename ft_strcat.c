#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	i;
	int	x;

	i = ft_strlen(dest + 1);
	x = 0;
	i++;
	while (src[x] != '\0')
	{
		dest[i] = src[x];
		i++;
		x++;
	}
	dest[i] = '\0';
	return (dest);
}
